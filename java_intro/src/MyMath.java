public class MyMath {


    public static int add(int a, int b) {
        return (a + b);
    }

    public static int sub(int a, int b) {
        return (a - b);
    }

    public static double mul(int a, int b) {
        return (a * b);
    }

    public static int div(int a, int b) {
        return (a / b);
    }

    public static int mod(int a, int b) {
        return (a % b);
    }
}
